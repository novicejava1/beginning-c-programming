/* Character and numbers example */
#include <stdio.h>

int main(void)
{
	char first = 'T';
	char second = 63;

	printf("The first example as a letter is : %c \n", first);
	printf("The first example as a number is : %d \n", first);
	printf("The second example as a letter is : %c \n", second);
	printf("The second example as a number is : %d \n", second);

	printf("Hexa decimal value of first letter is : %x \n", first);
	printf("Hexa decimal value of second letter is : %x \n", second);


	return 0;
}

